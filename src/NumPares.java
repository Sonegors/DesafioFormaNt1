
public class NumPares {
    public static void main(String[] args) {
        // Laço de repetição para percorrer os números de 0 a 100
        for (int i = 0; i <= 100; i++) {
            // Verifica se o número é par
            if (i % 2 == 0) {
                System.out.println(i); // Imprime o número par
            }
        }
    }
}

